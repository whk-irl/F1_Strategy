"""F1 race strategy Gymnasium environment.

The agent controls a single driver's tyre strategy (when to pit, which
compound).  All field drivers replay their actual recorded lap times from
the gold layer, giving the agent a realistic competitive context.

The agent's lap times are generated by the LightGBM tyre-degradation model,
blended with physics-based synthetic degradation curves.

Observation space (20 float32 values):
    0  race_progress            fraction of race done (0–1)
    1  laps_remaining_norm      laps left / total laps
    2  own_tyre_life_norm       tyre age / 60 (clipped)
    3  own_compound_norm        compound_encoded / 5
    4  is_fresh_tyre            0 or 1
    5  own_synthetic_deg_norm   current degradation penalty / 5 (clipped ±1)
    6  position_norm            current position / n_drivers (lower = better)
    7  gap_ahead_s_norm         seconds to car ahead / 60 (clipped 0–1)
    8  gap_behind_s_norm        seconds to car behind / 60 (clipped 0–1)
    9  tyre_age_ahead_norm      car ahead tyre life / 60 (clipped 0–1)
   10  tyre_age_behind_norm     car behind tyre life / 60 (clipped 0–1)
   11  undercut_threat          1 if gap_behind<3s AND car behind has fresher tyres
   12  overcut_opportunity      1 if car ahead pitted in the last 2 laps
   13  free_stop_available      1 if SC/VSC active this lap (track_status >= 2)
   14  cars_lose_if_pit_norm    cars in pit window ahead / n_drivers
   15  pit_loss_norm            circuit pit loss time / 30
   16  sc_probability           SC onset probability from SC model (0–1)
   17  sc_laps_since_last_norm  laps since last SC / 50 (clipped)
   18  is_wet                   1 if >30% of field on INTER/WET this lap
   19  wet_fraction             fraction of field on wet compounds this lap

Action space (Discrete 4):
    0  Stay out
    1  Pit for SOFT   (compound_encoded = 0)
    2  Pit for MEDIUM (compound_encoded = 1)
    3  Pit for HARD   (compound_encoded = 2)

Reward:
    Per-lap:
        -sim_lap_time_s / 100                           (penalises slow laps)
        - over_cliff * 0.20 * cliff_urgency             (cliff penalty, fades in last 12 laps)
        - 20.0 if pit with ≤4 laps remaining            (hard veto on unrecoverable late pits)
        + max(0, position_change) * 1.0                 (bonus for gaining positions)
        - max(0, -position_change) * 0.5                (penalty for losing positions)
        + position_norm * 0.1                           (persistent track position signal)
        + 5.0 if pitting during SC/VSC                  (free stop bonus)
    Terminal: position_norm * 50  (P1 → +50, last place → 0)
"""

