# ---------------------------------------------------------------------------
# Synthetic tyre degradation — base values (fleet average, neutral conditions)
#
# compound_encoded:  0=SOFT  1=MEDIUM  2=HARD  3=INTER  4=WET  5=UNKNOWN
# ---------------------------------------------------------------------------
